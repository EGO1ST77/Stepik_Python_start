# –ø–æ –≤–æ–∑—Ä–∞—Å—Ç–∞–Ω–∏—é —Å–ø–∏—Å–∫–∞, —Å–æ—Å—Ç–æ—è—â–∏—Ö –∏–∑ —Ü–µ–ª—ã—Ö —á–∏—Å–µ–ª, –∏ –æ–±—ä–µ–¥–∏–Ω—è–µ—Ç –∏—Ö –≤ –æ–¥–∏–Ω –æ—Ç—Å–æ—Ä—Ç–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫.
#
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 1. –°–ø–∏—Å–∫–∏ list1 –∏ list2 –º–æ–≥—É—Ç –∏–º–µ—Ç—å —Ä–∞–∑–Ω—É—é –¥–ª–∏–Ω—É.
#
# –ü—Ä–∏–º–µ—á–∞–Ω–∏–µ 2. –ú–æ–∂–Ω–æ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞—Ç—å —Å–ø–∏—Å–æ—á–Ω—ã–π –º–µ—Ç–æ–¥ sort(), –∞ –º–æ–∂–Ω–æ –æ–±–æ–π—Ç–∏—Å—å –∏ –±–µ–∑ –Ω–µ–≥–æ üòé

# –æ–±—ä—è–≤–ª–µ–Ω–∏–µ —Ñ—É–Ω–∫—Ü–∏–∏
def merge(list1, list2):
    list1.extend(list2)
    a = len(list1)
    for i in range(a-1):
        for j in range(i+1, a):
            if list1[i] > list1[j]:
                list1[i], list1[j] = list1[j], list1[i]

    return list1
# —Å—á–∏—Ç—ã–≤–∞–µ–º –¥–∞–Ω–Ω—ã–µ
numbers1 = [int(c) for c in input('–ü–µ—Ä–≤—ã–π —Å–ø–∏—Å–æ–∫ : ').split()]
numbers2 = [int(c) for c in input('–í—Ç–æ—Ä–æ–π —Å–ø–∏—Å–æ–∫ : ').split()]

# –≤—ã–∑—ã–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
print(merge(numbers1, numbers2))